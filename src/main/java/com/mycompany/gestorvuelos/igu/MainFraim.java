    private static DefaultTableModel convertirListaATabla(List<Aeropuerto> listAeropuertos) {
        /*
        this.codigoIATA = codigoIATA;
        this.nombre = nombre;
        this.codigoMunicipio = codigoMunicipio;
        */
        // Columnas del modelo
        String[] columnas = {"Codigo IATA", "Nombre", "Codigo Municipio"};

        // Datos del modelo
        Object[][] datos = new Object[listAeropuertos.size()][columnas.length];

        // Llenar los datos del modelo con la informaci√≥n de las personas
        for (int i = 0; i < listAeropuertos.size(); i++) {
            Aeropuerto aeropuerto = listAeropuertos.get(i);
            datos[i][0] = aeropuerto.getCodigoIATA();
            datos[i][1] = aeropuerto.getNombre();
            datos[i][2] = aeropuerto.getCodigoMunicipio();
        }

        // Crear el DefaultTableModel
        DefaultTableModel modelo = new DefaultTableModel(datos, columnas);

        return modelo;
    }

